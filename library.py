import uuid


class UniqueBook:
    def __init__(self, name: str, author: str):
        self.name = name
        self.author = author
        self.book_id = str(uuid.uuid4())

    def __str__(self):
        return self.name

    def __add__(self, other):
        return UniqueBook(self.name + self.name, self.author + other.author)


b1 = UniqueBook("asdas", "aksdasd")
b2 = UniqueBook("asdas", "aksdasd")

print(b1.book_id)
